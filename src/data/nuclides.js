// Build the final nuclide list with monoisotopic flags set.
function buildNuclides() {
  const list = [];
  for (const el of ELEMENTS) {
    let isos = ISOTOPES_RAW[el.sym];
    if (!isos) {
      const m = REMAINING_MONO[el.sym];
      if (!m) continue;
      isos = [m];
    }
    // find max-abundance
    let maxIdx = 0;
    for (let i = 1; i < isos.length; i++) {
      if (isos[i].abundance > isos[maxIdx].abundance) maxIdx = i;
    }
    for (let i = 0; i < isos.length; i++) {
      const iso = isos[i];
      list.push({
        key: `${el.sym}-${iso.A}`,
        element: el.sym,
        Z: el.Z,
        massNumber: iso.A,
        exactMass: iso.mass,
        abundance: iso.abundance,
        valence: defaultValenceFor(el.sym),
        isMonoisotopic: i === maxIdx
      });
    }
  }
  return list;
}

export const NUCLIDES = buildNuclides();
export const NUCLIDES_BY_KEY = Object.fromEntries(NUCLIDES.map((n) => [n.key, n]));

// Group by element for the isotope picker.
export function isotopesForElement(sym) {
  return NUCLIDES.filter((n) => n.element === sym);
}

// The monoisotope for a given element (used for default seeding).
export function monoisotopeFor(sym) {
  return NUCLIDES.find((n) => n.element === sym && n.isMonoisotopic);
}
